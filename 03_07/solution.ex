defmodule Solution do
  @moduledoc """
  This module contains a solution for the challenge in the previous challenge. It completes the two
  functions in the module: multiplier/2 and multiplier/3
  """

  @doc """
  Multiply two arguments
  """
  def multiplier(a, b) do
    a * b
  end

  @doc """
  Multiply three arguments
  """
  def multiplier(a, b, c) do
    a * b * c
  end
end
